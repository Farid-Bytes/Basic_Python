percentage = float(input("Enter Your Percentage:"))
if percentage >=90:
    print("Your grade is A+")
elif percentage >=80:
    print("Your grade is A")
elif percentage >=70:
    print("Your grade is B")
elif percentage >=60:
    print("Your grade is C")
elif percentage >=50:
    print("Your grade is D")
elif percentage >=40:
    print("Your grade is E")
else:
    print("Your grade is F")